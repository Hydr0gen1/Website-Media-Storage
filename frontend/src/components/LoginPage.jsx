export default function LoginPage({ onLogin }) {
  const [mode, setMode] = useState('login');
  const [username, setUsername] = useState('');
  const [password, setPassword] = useState('');
  const [confirm, setConfirm] = useState('');
  const [error, setError] = useState('');
  const [loading, setLoading] = useState(false);

  const reset = () => { setError(''); setUsername(''); setPassword(''); setConfirm(''); };
  const switchMode = (m) => { reset(); setMode(m); };

  const handleSubmit = async (e) => {
    e.preventDefault();
    setError('');
    if (mode === 'register' && password !== confirm) {
      return setError('Passwords do not match');
    }
    setLoading(true);
    try {
      const endpoint = mode === 'login' ? '/login' : '/register';
      const { data } = await axios.post(`${API_BASE}/auth${endpoint}`, { username, password });
      onLogin(data.user, data.token);
    } catch (err) {
      setError(err.response?.data?.error || 'Something went wrong');
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="login-page">
      <div className="login-card">
        {/* Branding */}
        <div className="login-brand">
          <span className="login-brand-icon">🎬</span>
          <h1 className="login-brand-name">MediaStore</h1>
          <p className="login-brand-tagline">Your personal media library</p>
        </div>

        {/* Mode tabs */}
        <div className="auth-tabs">
          <button
            className={`auth-tab ${mode === 'login' ? 'active' : ''}`}
            onClick={() => switchMode('login')}
          >
            Sign In
          </button>
          <button
            className={`auth-tab ${mode === 'register' ? 'active' : ''}`}
            onClick={() => switchMode('register')}
          >
            Create Account
          </button>
        </div>

        {/* OAuth */}
        <div className="oauth-buttons">
          {PROVIDERS.map(({ id, label, icon }) => (
            <a key={id} href={`${API_BASE}/auth/oauth/${id}`} className={`btn-oauth btn-oauth-${id}`}>
              {icon}
              {label}
            </a>
          ))}
        </div>

        <div className="oauth-divider"><span>or</span></div>

        {/* Local form */}
        <form onSubmit={handleSubmit} className="auth-form">
          <div className="form-field">
            <label htmlFor="lp-username">Username</label>
            <input
              id="lp-username"
              type="text"
              value={username}
              onChange={(e) => setUsername(e.target.value)}
              autoComplete="username"
              autoFocus
              required
            />
          </div>
          <div className="form-field">
            <label htmlFor="lp-password">Password</label>
            <input
              id="lp-password"
              type="password"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              autoComplete={mode === 'login' ? 'current-password' : 'new-password'}
              required
            />
          </div>
          {mode === 'register' && (
            <div className="form-field">
              <label htmlFor="lp-confirm">Confirm Password</label>
              <input
                id="lp-confirm"
                type="password"
                value={confirm}
                onChange={(e) => setConfirm(e.target.value)}
                autoComplete="new-password"
                required
              />
            </div>
          )}

          {error && <p className="auth-error">{error}</p>}

          <button type="submit" className="btn btn-primary login-submit" disabled={loading}>
            {loading ? '…' : mode === 'login' ? 'Sign In' : 'Create Account'}
          </button>
        </form>
      </div>
    </div>
  );
}
